                <h2>What is a smart home?</h2>
                <p class="lead">Connecting the tech in your home to your Wi-Fi network (and to each other) has a few benefits. First up is controls and convenience. You can control everything in real time either from an app on your smartphone or tablet, or from a voice controlled smart speaker. We’ll get to these in a minute. A lot of smart home companies are trying to sell convenience – they will make your life a little easier or save you time on a regular basis.<br><br>

Second, there's information. Smart home gadgets can give you easily accessible data on things like security – say, access to a history of smart security camera feeds – to energy – smart meters and energy monitors that tell you (and your utility provider) how much electricity and gas you’re getting through.<br><br>


 
Third is automation. If you don’t want to manually control your home gadgets all the time you can go one further and set up scenes, routines and rules. Depending on things like you entering or leaving the house/flat or sensors being triggered, you can set up certain actions as a result. The idea is that your home gets to know you and your family/friends and automatically works based on what’s happening, without the need for your input.<br><br>

Lastly (and this is only really a taster), well we get to robots. The next steps up from lights that turn red when your smoke alarm goes off are autonomous gadgets that operate themselves. We’re talking robot vacuums, robot lawn mowers and laundry folding robots, all of which currently exist – but generally this category involves plenty of wishful thinking. The smart home can’t do all of your household chores for you – yet.</p>
